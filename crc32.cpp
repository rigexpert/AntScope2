quint32 CRC32::crc(quint32 crc32, const QByteArray &data)
{
    const char *buf = data.constData();

    for (int i = 0; i < data.length(); i++)
    {
        crc32 = (crc32 >> 8) ^ CRC32Table[(crc32 ^ buf[i]) & 0xff];
    }

    crc32 ^= 0xffffffff;
    return crc32;
}

quint8 CRC32::crc8(const QByteArray& buf)
{
    quint8 crc8_calculation_register = 0;

    for(int i = 0; i < buf.length(); ++i)
    {
        crc8_calculation_register = CRC8Table[crc8_calculation_register ^ (unsigned char)(buf.at(i))];
        crc8_calculation_register &= 0xFF;
    }

    return crc8_calculation_register;
}
